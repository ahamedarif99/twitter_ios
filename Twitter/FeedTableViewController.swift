    func loadTweet(){
        let myURL = "https://api.twitter.com/1.1/statuses/home_timeline.json"
        let myPrams = ["count": 10]
        TwitterAPICaller.client?.getDictionariesRequest(url: myURL, parameters: myPrams, success: { (tweets: [NSDictionary]) in
            self.tweetARR.removeAll()
            for tweet in tweets {
                self.tweetARR.append(tweet)
            }
            
            self.tableView.reloadData()
        }, failure: { (Error) in
            print("Could not get tweet!")
        })
    }
    
    
    @IBAction func onLogout(_ sender: Any) {
